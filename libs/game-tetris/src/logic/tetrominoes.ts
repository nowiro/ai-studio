/** All 7 piece kinds — useful for the 7-bag randomiser. */
export const ALL_KINDS: readonly TetrominoKind[] = ['I', 'O', 'T', 'S', 'Z', 'J', 'L'];

/** Standard spawn position (top-centre of the playfield). */
export function spawnPosition(cols: number): CellPos {
  // Pieces are 4×4 grids; centring on a 10-wide board lands the matrix at x=3.
  return { x: Math.floor((cols - 4) / 2) + 1, y: -2 };
}

/** Return the filled cells (relative offsets) for a piece in a given rotation. */
export function shapeCells(kind: TetrominoKind, rotation: Rotation): readonly CellPos[] {
  const matrix = TETROMINOES[kind][rotation];
  const cells: CellPos[] = [];
  for (let row = 0; row < matrix.length; row++) {
    const r = matrix[row];
    if (!r) continue;
    for (let col = 0; col < r.length; col++) {
      if (r[col] === 1) cells.push({ x: col, y: row });
    }
  }
  return cells;
}
