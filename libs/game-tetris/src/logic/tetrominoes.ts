/**
 * Tetromino shape definitions (Tetris Guideline). Each piece is a 4×4 matrix
 * per rotation (0/R/2/L = north/east/south/west). `1` = filled cell, `0` = empty.
 *
 * Centring matters: pieces are spawned with `position.x` such that the leftmost
 * filled column lands at column 3 (the "left" of the standard spawn slot), and
 * `position.y = -2` so the piece appears in the buffer zone above the visible
 * playfield. The board does NOT need a `y: -2` row — we render only `y >= 0`.
 */
import type { CellPos, Rotation, TetrominoKind } from '../types.js';

/** A 4×4 occupancy grid. Outer = rows, inner = cols. */
export type ShapeMatrix = readonly (readonly (0 | 1)[])[];

/** Per-rotation shape matrix. */
