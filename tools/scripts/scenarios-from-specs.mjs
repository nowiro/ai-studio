#!/usr/bin/env node
/**
 * scenarios-from-specs.mjs — extract Given/When/Then triples from analytical specs and
 * emit Playwright scenario skeletons under `tmp/scenarios/`.
 *
 * The skeletons are starting points only — the **test-scenario-author** moves them into
 * `apps/<app>-e2e/src/specs/` and the **test-engineer** fleshes out fixtures + assertions.
 *
 * Pure: no LLM, no network. Deterministic given the same input specs.
 */
import { mkdir, readdir, readFile, stat, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { basename, join, relative, resolve } from 'node:path';
import process from 'node:process';

const ROOT = process.cwd();
const SPECS_DIR = resolve(ROOT, 'docs/analytical/specs');
const OUT_DIR = resolve(ROOT, 'tmp/scenarios');

async function listSpecs() {
  if (!existsSync(SPECS_DIR)) return [];
  const entries = await readdir(SPECS_DIR, { withFileTypes: true });
  return entries
    .filter((e) => e.isFile() && e.name.endsWith('.md') && e.name !== '.gitkeep')
    .map((e) => join(SPECS_DIR, e.name));
}

/**
 * Match Given/When/Then blocks. Tolerates two shapes:
 *   1. Indented list under "Acceptance criteria":
 *        - Given X
 *          When Y
 *          Then Z
 *   2. Free-form paragraph:
 *        Given X
 *        When Y
 *        Then Z
 */
function extractScenarios(text) {
  const re = /Given\s+([^\n]+?)[\s\S]*?When\s+([^\n]+?)[\s\S]*?Then\s+([^\n]+?)(?=\n\s*(?:Given\b|##|$))/g;
  const out = [];
  let m;
  while ((m = re.exec(text)) !== null) {
    out.push({
      given: m[1].trim().replace(/\s+/g, ' '),
      when: m[2].trim().replace(/\s+/g, ' '),
      then: m[3].trim().replace(/\s+/g, ' '),
      sourceLine: text.slice(0, m.index).split('\n').length,
    });
  }
  return out;
}

function safeTitle(s) {
  return s
    .replace(/[^a-zA-Z0-9 ]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
    .slice(0, 60);
}

function renderSkeleton({ specPath, specTitle, scenarios }) {
  const importLines = `import { expect, test } from '@playwright/test';\n`;
  const header = `\n/**\n * Generated by tools/scripts/scenarios-from-specs.mjs.\n * Source spec: ${specPath}\n *\n * Hand-off to the **test-engineer** to add fixtures, network stubs and final assertions.\n * Replace each \`// TODO\` with a concrete \`page.…\` call. Use getByRole > getByTestId > CSS.\n */\n`;
  const suite = `\ntest.describe('${specTitle}', () => {\n${scenarios
    .map(
      (s, i) => `  test('scenario ${i + 1}: ${safeTitle(s.when)}', async ({ page }) => {
    // GIVEN: ${s.given}
    // from spec: ${specPath}:${s.sourceLine}
    await page.goto('/');
    // TODO (test-engineer): set up fixtures matching the GIVEN clause.

    // WHEN: ${s.when}
    // TODO (test-engineer): drive the UI matching the WHEN clause.

    // THEN: ${s.then}
    // TODO (test-engineer): assert observable outcome matching the THEN clause.
    expect.soft(true, 'placeholder — replace with real assertion').toBeTruthy();
  });`,
    )
    .join('\n\n')}\n});\n`;
  return importLines + header + suite;
}

async function main() {
  const specs = await listSpecs();
  if (specs.length === 0) {
    console.log('ℹ No specs under docs/analytical/specs/ — nothing to extract.');
    return;
  }

  await mkdir(OUT_DIR, { recursive: true });

  const summary = [];
  for (const f of specs) {
    const txt = await readFile(f, 'utf8');
    const titleMatch = txt.match(/^#\s+(.+)$/m);
    const scenarios = extractScenarios(txt);
    const slug = basename(f, '.md');
    const specPath = relative(ROOT, f).replace(/\\/g, '/');

    if (scenarios.length === 0) {
      summary.push({ spec: specPath, scenarios: 0, skipped: true });
      continue;
    }

    const meta = {
      spec: specPath,
      title: titleMatch?.[1]?.trim() ?? slug,
      scenarios,
      generatedAt: new Date().toISOString(),
    };
    const skeleton = renderSkeleton({
      specPath,
      specTitle: meta.title,
      scenarios,
    });

    await writeFile(join(OUT_DIR, `${slug}.json`), JSON.stringify(meta, null, 2));
    await writeFile(join(OUT_DIR, `${slug}.spec.ts`), skeleton);
    summary.push({ spec: specPath, scenarios: scenarios.length, skipped: false });
  }

  const total = summary.reduce((n, s) => n + s.scenarios, 0);
  console.log(`✓ Generated ${total} scenarios across ${summary.filter((s) => !s.skipped).length} specs → ${relative(ROOT, OUT_DIR)}/`);
  for (const s of summary) {
    if (s.skipped) console.log(`  · skip ${s.spec} (no Given/When/Then found)`);
    else console.log(`  · ${s.spec} → ${s.scenarios} scenario${s.scenarios > 1 ? 's' : ''}`);
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
